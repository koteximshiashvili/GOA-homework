def filter_odd(list):
    list1 =[]
    for i in list:
        if i % 2 == 0:
            print(list1.append(i))
    print(list1)
   

print(filter_odd([36 , 57 , 90 , 42]))


def even_sum(odd):
    list2 =[]
    for i in odd:
        if i % 2 == 0:
            list2.append(i)

        return sum(list2)
print(even_sum([56 , 77 , 23 , 44 , 68]))
